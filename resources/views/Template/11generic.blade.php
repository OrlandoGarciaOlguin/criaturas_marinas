							<h2>EL MITO DE ESCILA Y CARIBDIS</h2>
							<p>El mito tal como aparece en la Odisea de Homero describe la existencia en una zona de un mar con un canal, en el que en un lado existe un monstruo, Escila, con torso de mujer y cola de pez del que surgen seis perros, cada uno con dos patas finalizados en cabezas con tres filas de dientes que atacan ferozmente a los barcos que pasan por su lado del estrecho. </p>
							<p>Y en el otro lado del canal se forma un remolino, Caribdis, que de forma periódica-tres veces al día-atrapa los barcos que pasan por su lado y al cabo los devuelve destrozados. </p>
							<p>Y como el canal es muy estrecho, al pasar los barcos corren un gran peligro, pues si no caen en las garras de Escila lo hacen en las olas de Caribdis. De hecho, en su viaje de vuelta a casa que se narra en la Odisea, y por consejo de la diosa Circe, Ulises pasó por el lado de Escila pues así perdía “sólo” seis marineros pero no perdía todo el barco.</p>
							<p>Este mito ha aparecido en numerosas obras literarias-desde Homero, Ovidio y Virgilio a Manzoni y James Joyce -y artísticas, por ejemplo en pinturas, cerámicas, fuentes, esculturas, monedas…</p>
							<p>E incluso, se ha establecido en el lenguaje periodístico, médico, legal etc. para calificar situaciones muy peligrosas, pues aparentemente para salir de ellas sólo hay dos salidas, pero las dos son malas. </p>
							<p> Y en el lenguaje vulgar, el proverbio que invoca este mito significa estar entre “espada y la pared”.</p>
