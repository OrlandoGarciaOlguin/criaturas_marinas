							<p>¿Tiene hermanos o familia a la que pueda considerar cercana? ¿Se reproduce? ¿Es el mismo Nessie de hace mil años o son un grupo de individuos con características muy parecidas? ¿Cómo nació? Son muchísimas las preguntas acerca del monstruo del lago Ness, pero el consenso general es parecido: en caso de existir, por las características que se le atribuyen, sería algo así como una criatura semejante a los extintos plesiosaurios (criaturas acuáticas prehistóricas). Los fósiles que hay de ellos corresponden con el mesozoico. </p>
